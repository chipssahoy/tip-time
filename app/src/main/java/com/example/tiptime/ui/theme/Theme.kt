@Composable
fun TipTimeTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    themeOption: ThemeOption = ThemeOption.BLUE,
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        else -> when (themeOption) {
            ThemeOption.BLUE -> if (darkTheme) BlueDarkColorScheme else BlueColorScheme
            ThemeOption.PURPLE -> if (darkTheme) PurpleDarkColorScheme else PurpleColorScheme
            ThemeOption.GREEN -> if (darkTheme) GreenDarkColorScheme else GreenColorScheme
            ThemeOption.YELLOW -> if (darkTheme) YellowDarkColorScheme else YellowColorScheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

// Custom text field colors
@Composable
fun tipTimeTextFieldColors() = TextFieldDefaults.colors(
    focusedContainerColor = MaterialTheme.colorScheme.surfaceVariant,
    unfocusedContainerColor = MaterialTheme.colorScheme.surfaceVariant,
    disabledContainerColor = MaterialTheme.colorScheme.surfaceVariant,
    focusedIndicatorColor = MaterialTheme.colorScheme.primary,
    unfocusedIndicatorColor = MaterialTheme.colorScheme.outline,
    cursorColor = MaterialTheme.colorScheme.primary,
    focusedLabelColor = MaterialTheme.colorScheme.primary,
    unfocusedLabelColor = MaterialTheme.colorScheme.onSurfaceVariant,
    focusedTextColor = MaterialTheme.colorScheme.onSurface,
    unfocusedTextColor = MaterialTheme.colorScheme.onSurface
)

// Custom switch colors
@Composable
fun tipTimeSwitchColors() = SwitchDefaults.colors(
    checkedThumbColor = MaterialTheme.colorScheme.secondary,
    checkedTrackColor = MaterialTheme.colorScheme.secondaryContainer,
    checkedBorderColor = MaterialTheme.colorScheme.secondary,
    uncheckedThumbColor = MaterialTheme.colorScheme.outline,
    uncheckedTrackColor = MaterialTheme.colorScheme.surfaceVariant,
    uncheckedBorderColor = MaterialTheme.colorScheme.outline
)

// Custom icon button colors
@Composable
fun tipTimeIconButtonColors() = IconButtonDefaults.iconButtonColors(
    containerColor = MaterialTheme.colorScheme.surfaceVariant,
    contentColor = MaterialTheme.colorScheme.primary
)
